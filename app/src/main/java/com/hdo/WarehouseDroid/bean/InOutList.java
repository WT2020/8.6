package com.hdo.WarehouseDroid.bean;

import java.io.Serializable;

/**
 * description 出入库申请获取基类
 * author 张建银
 * version 1.0
 * Created on 2017/12/15.
 */

public class InOutList implements Serializable {

    //公共部分
    private int id;//ID
    private String warehouse;//仓库
    private String remark;//备注
    private String number;//单号
    private int state;//状态(1:申请;2:审核;3:报废/...;)

    //入库部分
    private String inOpDate;//入库时间
    private String whereFrom;//来源
    private String inUser;//入库人

    //出库部分
    private String outOpDate;//出库时间
    private String whereGo;//去向
    private String outUser;//出库人

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getWarehouse() {
        return warehouse;
    }

    public void setWarehouse(String warehouse) {
        this.warehouse = warehouse;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public String getInOpDate() {
        return inOpDate;
    }

    public void setInOpDate(String inOpDate) {
        this.inOpDate = inOpDate;
    }

    public String getWhereFrom() {
        return whereFrom;
    }

    public void setWhereFrom(String whereFrom) {
        this.whereFrom = whereFrom;
    }

    public String getInUser() {
        return inUser;
    }

    public void setInUser(String inUser) {
        this.inUser = inUser;
    }

    public String getOutOpDate() {
        return outOpDate;
    }

    public void setOutOpDate(String outOpDate) {
        this.outOpDate = outOpDate;
    }

    public String getWhereGo() {
        return whereGo;
    }

    public void setWhereGo(String whereGo) {
        this.whereGo = whereGo;
    }

    public String getOutUser() {
        return outUser;
    }

    public void setOutUser(String outUser) {
        this.outUser = outUser;
    }
}
