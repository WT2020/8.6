package com.hdo.WarehouseDroid.bean;

import java.io.Serializable;

/**
 * 项目名称：WarehouseDroid
 * 创建人:吴小雪
 * 创建时间:2017/12/14  16:34
 */

public class User implements Serializable {

    private static final long serialVersionUID = 1L;


    private Integer id;//主键
    private String number;//工号
    private String password;//密码
    private String name;//姓名
    private String phone;//电话
    private String identity;//身份
    private Department department;//部门
    private int state = 1;//账号状态(1：正常;0:停用)
    private String token;//移动端身份认证
    private Integer monthLoginNum=0;//本月登录次数
    private String thisMonth;//本月
    private Integer totalLoginNum=0;//总计登陆次数
    private String userType;//用户类型


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getIdentity() {
        return identity;
    }

    public void setIdentity(String identity) {
        this.identity = identity;
    }

    public Department getDepartment() {
        return department;
    }

    public void setDepartment(Department department) {
        this.department = department;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }


    public Integer getMonthLoginNum() {
        return monthLoginNum;
    }

    public void setMonthLoginNum(Integer monthLoginNum) {
        this.monthLoginNum = monthLoginNum;
    }

    public String getThisMonth() {
        return thisMonth;
    }

    public void setThisMonth(String thisMonth) {
        this.thisMonth = thisMonth;
    }

    public Integer getTotalLoginNum() {
        return totalLoginNum;
    }

    public void setTotalLoginNum(Integer totalLoginNum) {
        this.totalLoginNum = totalLoginNum;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public User() {
        super();
    }

}
